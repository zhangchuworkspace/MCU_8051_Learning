#include <REGX52.H>
#include "delay.h"
#include "MatrixLED.h"

unsigned char Animation[]={
0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
0xFF,0x10,0x10,0x10,0xFF,0x00,0xFF,0x91,
0x91,0x91,0x00,0xFF,0x80,0x80,0x80,0x00,
0xFF,0x80,0x80,0x80,0x00,0x3C,0x42,0xC3,
0x81,0x81,0xC3,0x42,0x3C,0x00,0x00,0x00,
0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
};

void main()
{
	unsigned char i,offset=1,Count=0;
	MatrixLED_Init();
	while(1)
	{
		for(i=0;i<8;i++){
			MatrixLED_ShowColumn(i,Animation[i+offset]);
		}
		Count++;
		if(Count>10){
			Count=0;
			offset++;
			if(offset>32){
				offset=0;
			}
		}
	}
}
